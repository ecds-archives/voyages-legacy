package edu.emory.library.tas;

public class Dictionary {
	private Long id;
	private String name;
	
	public Dictionary() {
	}

	public Long getId() {
		return id;
	}

	private void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	private void setName(String name) {
		this.name = name;
	}
	
	
}
