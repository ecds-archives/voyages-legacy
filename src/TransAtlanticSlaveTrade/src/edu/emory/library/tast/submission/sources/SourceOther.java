package edu.emory.library.tast.submission.sources;

public class SourceOther {
	
	private String title;
	private String location;
	private String pageOrFolio;
	private String note;
	
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
	public String getPageOrFolio() {
		return pageOrFolio;
	}
	public void setPageOrFolio(String pageOrFolio) {
		this.pageOrFolio = pageOrFolio;
	}
	
}
